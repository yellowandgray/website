                            <h4>The ABCs of I Can Read</h4>
                            <h5>The Joy of Reading</h5>
                            <p>At I Can Read, we love to share the joy of reading and the expansion of knowledge that comes with it. We want your child’s literacy journey to begin early and with the support necessary for educational success. Our primary focus is to equip children with the tools they need to overcome any difficulties they may face while learning how to read. We employ our unique methodology to address any potential obstacles that may arise on the path to independent reading. Our attentive reading specialists look out for any hardships throughout the process and assist each child in overcoming these difficulties allowing them to develop into confident and independent readers. The longest journey begins with the first step. And the first step is to join I Can Read.</p>
                            <h5>The I Can Read Secret</h5>
                            <p>The I Can Read formula was developed by two leading educational psychologists in Australia studying child psychology, English literacy and linguistic science over a 15 year period. The result of this extensive research is a reading program based on the smallest possible unit of sound – called a phoneme -rather than simply an alphabet letter - as the driving force behind learning to read. The blending of these phonemes together unlocks the magic and enables children to overcome any challenges giving them an intimate understanding of how to read independently. Our formula is very much more than a mere phonics program. Numerous test studies measuring the superior reading aptitude of I Can Read vs non I Can Read students are a testament to the success of our methodology. More than 200,000 students in eight countries around the world have benefited from our program are a part of this success story.</p>
                            <h5>Invest in Education and Plant a Seed for the Future</h5>
                            <p>Parents all around the world want the best for their children and definitely want to invesst in their future to the best of their ability. Other than the joy that comes with helping children succeed and providing a widely respected service to the community, the I Can Read brand itself is a solid financial investment.  Our business yields a high return on investment (ROI) as many of our franchisee partners have discovered.  </p>
                            <p>When you invest in an I Can Read Franchise, you will immediately gain access to solid brand recognition, our world-famous curriculum, proven business know-how and ongoing market research into the latest teaching methods and systems development ensuring we are always two steps ahead of the competition.  You will benefit from our thorough experience with the business model showing you how to realize significant cost reductions by utilizing economies of scale. To be a successful franchisee, a good business sense is required. You do not need an intimate knowledge of academics as we have an ongoing training and support program to ensure you are well versed in the I Can Read methodology.</p>
