                            <h3 class="text-center color-blue">How big is the global enrichment industry?</h3>
                            <p>In 2012, market research company Global Industry Analysts, Inc (GIA) announced the global private tutoring market will surpass US$102 billion (S$140 billion) by 2018. Countries in Asia, notably Hong Kong, Japan, Singapore, South Korea and China contribute significantly to the global private tutoring market. South Korea alone was projected to reach US$14 billion – or 15 percent of the entire market – by 2012.</p>
                            <p>Another survey done by MasterCard a year later revealed parents in Asia Pacific spent, on average, 14 percent of their monthly household income on enrichment classes. The study also revealed more than two thirds of the households in Asia Pacific are willing to spend on enrichment classes – which are extra spending on top of school fees – for their children. More than half of parents in Thailand (52 percent) are spending on extra tuition while households in Malaysia and Singapore trail just behind at 46 and 45 percent respectively. Adding to MasterCard’s Consumer Purchasing Priorities – Education survey, various stakeholders say the trend is hard to miss. While more established countries such as South Korea, Taiwan, Hong Kong, Singapore and Malaysia had traditionally led the pack, Vietnam, Thailand, Indonesia and India are now marked to show significant growth as well.</p>
                            <p>The MasterCard report also showed a large percentage of parents in China (53 percent), Thailand (43 percent) and Vietnam (40 percent) enrolling their children in foreign language enrichment classes.</p>
                            <p>Experts say the trend is upheld by Asian parents who feel the need to invest in education.</p>
                            <p>The strong economic growth of emerging countries such as Vietnam, India and China has contributed to a new affluent middle class. The meeting of these factors will push towards further growth in the education industry. Jumping on the bandwagon is the first enrichment mall in Malaysia. The School, located within Jaya One in Petaling Jaya, offers a mix of tenants but its draw is the 40 percent of businesses that offer creative learning in the arts, sciences, languages and arithmetic. The brains behind the concept say two in three mothers in Malaysia enrol their children in up to two enrichment classes a week. They surmised it would be wise to offer parents a place to shop and catch up on errands while their children attend extracurricular classes.  </p>
                            <p>From playing catch up to leading the pack, China’s private kindergartens are embracing Western-style play learning and mixing it with project-based learning. These micro private schools often have their own curriculum separate from the official education system, which is often seen by parents as oppressive. Parents who have enrolled children in these types of private educational platforms say their children love it and even refuse to take leave when they are ill.</p>
                            <p>In an updated 2016 report, GIA said the global private tutoring market is forecasted to reach US$227 billion by 2022. This is in part driven by a need to keep pace with classroom teaching and enhance knowledge and skill level. The report said “higher education is linked to better opportunities in the job market, higher remuneration and improved social standing”, which leads to parents investing heavily in their children’s educational foundation.</p>
                            <p>While a good tertiary education is important, parents know children require a solid foundation to stand on. A senior reporter opined in the Straits Times that she would send her children to enrichment classes to save them from the possible repercussions of falling behind in school.</p>
                            <p>The global private tutoring market is a mainstay in today’s knowledge economy. Can you afford to be left behind?</p>
